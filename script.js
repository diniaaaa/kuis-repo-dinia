let questions = [];
let currentQuestionIndex = 0;
let score = 0;

const questionEl = document.getElementById("question");
const optionsEl = document.getElementById("options");
const nextBtn = document.getElementById("next-btn");
const resultBox = document.getElementById("result-box");
const quizBox = document.getElementById("quiz-box");
const scoreEl = document.getElementById("score");

function shuffleArray(array) {
  return [...array].sort(() => Math.random() - 0.5);
}

function showQuestion() {
  const q = questions[currentQuestionIndex];
  questionEl.textContent = q.question;
  optionsEl.innerHTML = "";
  nextBtn.style.display = "none";

  q.options.forEach(option => {
    const btn = document.createElement("button");
    btn.textContent = option;
    btn.onclick = () => checkAnswer(btn, option);
    optionsEl.appendChild(btn);
  });
}

function checkAnswer(button, selected) {
  const correct = questions[currentQuestionIndex].answer;
  const allButtons = document.querySelectorAll("#options button");

  allButtons.forEach(btn => {
    btn.disabled = true;
    if (btn.textContent === correct) {
      btn.classList.add("correct");
    } else if (btn.textContent === selected) {
      btn.classList.add("wrong");
    }
  });

  if (selected === correct) {
    score++;
  }

  nextBtn.style.display = "block";
}

nextBtn.onclick = () => {
  currentQuestionIndex++;
  if (currentQuestionIndex < questions.length) {
    showQuestion();
  } else {
    showResult();
  }
};

function showResult() {
  
  quizBox.classList.add("hidden");
  
  // Menampilkan box hasil
  resultBox.classList.remove("hidden");

  // Menampilkan skor
  scoreEl.textContent = `${score} dari ${questions.length}`;

  // Pastikan kita hanya menampilkan 1 box hasil, bukan duplikat
  document.getElementById("score").innerHTML = `${score} dari ${questions.length}`;
}


function startQuiz() {
  currentQuestionIndex = 0;
  score = 0;
  questions = shuffleArray(allQuestions).slice(0, 10); // Ambil 10 soal acak
  resultBox.classList.add("hidden");
  quizBox.classList.remove("hidden");
  showQuestion();
}
document.getElementById("start-btn").onclick = () => {
  document.getElementById("intro-screen").style.display = "none";
  document.getElementById("quiz-container").classList.remove("hidden");
  startQuiz();
};


window.onload = startQuiz;

